# 5 – Usando type() identifique e mostre na tela o tipo das
# seguintes variáveis: 5, ‘Pedro’, 0.58, 890, 1==1,  ‘O vento levou’.

print('A variável 5 é do tipo: ', type(5))
print('A variável ‘Pedro’ é do tipo: ', type('Pedro'))
print('A variável 0.58 é do tipo: ', type(0.58))
print('A variável 890 é do tipo: ', type(890))
print('A variável 1==1 é do tipo: ', type(1==1))
print('A variável ''O vento levou'' é do tipo: ', type('O vento levou'))